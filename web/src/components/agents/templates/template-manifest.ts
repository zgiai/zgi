export function findTemplatesByPromptId(promptId: string): AgentTemplate[] {
  return AGENT_TEMPLATES.filter(template =>
    (template.recommendedPrompts || []).some(reference =>
      Object.values(reference.promptIdsByLocale).includes(promptId)
    )
  );
}
