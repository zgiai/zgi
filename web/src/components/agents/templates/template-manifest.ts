function templateYaml(fileName: string): Pick<AgentTemplate, 'yamlPath' | 'localizedYamlPaths'> {
  const localizedYamlPaths = {
    'en-US': `/templates/agents/en-US/${fileName}`,
    'zh-Hans': `/templates/agents/zh-Hans/${fileName}`,
  } satisfies AgentTemplate['localizedYamlPaths'];

  return {
    yamlPath: localizedYamlPaths['en-US'],
    localizedYamlPaths,
  };
}

