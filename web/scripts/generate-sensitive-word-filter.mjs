import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';

const rootDir = process.cwd();
const dictionaryDir = resolve(rootDir, 'sensitive-words');
const outputFile = resolve(rootDir, 'src/generated/sensitive-word-automaton.ts');

function normalizeSensitiveText(value) {
  return String(value ?? '')
    .normalize('NFKC')
    .toLowerCase()
    .replace(/[\p{White_Space}\p{Punctuation}\p{Symbol}]+/gu, '');
}

function collectTxtFiles(dir) {
  if (!existsSync(dir)) return [];

  const result = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const fullPath = join(dir, entry.name);
    if (entry.isDirectory()) {
      result.push(...collectTxtFiles(fullPath));
    } else if (entry.isFile() && entry.name.toLowerCase().endsWith('.txt')) {
      result.push(fullPath);
    }
  }
  return result;
}

function collectWords() {
  const words = new Set();
  for (const file of collectTxtFiles(dictionaryDir)) {
    const content = readFileSync(file, 'utf8').replace(/^\uFEFF/, '');
    for (const line of content.split(/\r?\n/)) {
      const normalized = normalizeSensitiveText(line.trim().replace(/^\uFEFF/, ''));
      if (normalized.length > 0) {
        words.add(normalized);
      }
    }
  }
  return [...words].sort();
}

function createNode() {
  return { transitions: {}, failure: 0, outputs: 0 };
}

function buildAutomaton(words) {
  const nodes = [createNode()];
  let maxWordLength = 0;

  for (const word of words) {
    let state = 0;
    let length = 0;
    for (const char of word) {
      length += 1;
      const next = nodes[state].transitions[char];
      if (typeof next === 'number') {
        state = next;
        continue;
      }
      const created = nodes.length;
      nodes[state].transitions[char] = created;
      nodes.push(createNode());
      state = created;
    }
    nodes[state].outputs += 1;
    maxWordLength = Math.max(maxWordLength, length);
  }

  const queue = [];
  for (const next of Object.values(nodes[0].transitions)) {
    nodes[next].failure = 0;
    queue.push(next);
  }

  for (let head = 0; head < queue.length; head += 1) {
    const state = queue[head];
    for (const [char, target] of Object.entries(nodes[state].transitions)) {
      let fallback = nodes[state].failure;
      while (fallback !== 0 && typeof nodes[fallback].transitions[char] !== 'number') {
        fallback = nodes[fallback].failure;
      }
      const fallbackTarget = nodes[fallback].transitions[char];
      nodes[target].failure = typeof fallbackTarget === 'number' ? fallbackTarget : 0;
      nodes[target].outputs += nodes[nodes[target].failure].outputs;
      queue.push(target);
    }
  }

  return {
    transitions: nodes.map(node => node.transitions),
    failures: nodes.map(node => node.failure),
    outputs: nodes.map(node => node.outputs),
    wordCount: words.length,
    maxWordLength,
  };
}

function writeAutomaton(data) {
  mkdirSync(dirname(outputFile), { recursive: true });
  const automatonJson = JSON.stringify(data);
  const source = [
    '// This file is generated by scripts/generate-sensitive-word-filter.mjs.',
    '// Do not edit by hand.',
    '',
    "import type { SensitiveWordAutomatonData } from '@/utils/sensitive-word-filter';",
    '',
    `const automatonJson = ${JSON.stringify(automatonJson)};`,
    '',
    'export const sensitiveWordAutomatonData = JSON.parse(automatonJson) as SensitiveWordAutomatonData;',
    '',
  ].join('\n');
  writeFileSync(outputFile, source, 'utf8');
}

writeAutomaton(buildAutomaton(collectWords()));
